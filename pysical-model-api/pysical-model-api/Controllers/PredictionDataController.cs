        private string GetRequestTime(DateTime date)
        {
            var validTimes = new string[] { "0200", "0500", "0800", "1100", "1400", "1700", "2000", "2300" };
            int index;
            var hour = date.Hour;
            var minute = date.Minute;
            var time = date.Hour * 100 + date.Minute;

            if (time > 210 && time <= 510) index = 0;
            else if (time > 510 && time <= 810) index = 1;
            else if (time > 810 && time <= 1110) index = 2;
            else if (time > 1110 && time <= 1410) index = 3;
            else if (time > 1410 && time <= 1710) index = 4;
            else if (time > 1710 && time <= 2010) index = 5;
            else if (time > 2010 && time <= 2310) index = 6;
            else index = 7;

            return validTimes[index];
        }

