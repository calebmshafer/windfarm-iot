        [HttpGet]
        public async Task<IEnumerable<PredictionData>> Get()
        {
            var windforecast = await GetWeatherData();

            var predictionData = new List<PredictionData>();

            foreach(var forecast in windforecast)
            {
                var data = new PredictionData
                {
                    forecastDateTime = forecast.forecastDateTime,
                    windspeed = forecast.windspeed,
                    winddirection = ChangeWindDir(forecast.winddir),
                    yawposition = GetYawPositionByWindDirection(forecast.winddir),
                    bladepitch1 = 2.0,
                    bladepitch2 = 2.0,
                    bladepitch3 = 2.0
                };
                predictionData.Add(data);
            }

            return predictionData;
        }

        private double GetYawPositionByWindDirection(double winddirection)
        {
            return winddirYawpos.Aggregate((x, y) => Math.Abs(x.WinDir - winddirection) < Math.Abs(y.WinDir - winddirection) ? x : y).YawPosition;
        }

        private static double Interpolate(DateTime x0, double y0, DateTime x1, double y1, DateTime target)
        {
            if (y0 == y1) return y0;
            var totalsec = (x1 - x0).TotalSeconds;
            double rate = (y1 - y0) / totalsec ;
            double xDistance = totalsec - (x1 - target).TotalSeconds;
            //and then return the datetime that goes along with that point
            return rate * xDistance + y0;
        }

        private double ChangeWindDir(double windirection360)
        {
            if (windirection360 >= 0 && windirection360 < 180) return windirection360;
            else return -(360.0 - windirection360);
        }

